package com.example;

public class Skin {
    
    private String id;
    private String name;
    private String description;
    private String weapon;
    private String pattern;
    private Number min_float;
    private Number max_float;
    private String rarity;
    private String image;



    public Skin(){

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getWeapon() {
        return weapon;
    }

    public void setWeapon(String weapon) {
        this.weapon = weapon;
    }

    public String getPattern() {
        return pattern;
    }

    public void setPattern(String pattern) {
        this.pattern = pattern;
    }

    public String getRarity() {
        return rarity;
    }

    public void setRarity(String rarity) {
        this.rarity = rarity;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Number getMin_float() {
        return min_float;
    }

    public void setMin_float(Number min_float) {
        this.min_float = min_float;
    }

    public Number getMax_float() {
        return max_float;
    }

    public void setMax_float(Number max_float) {
        this.max_float = max_float;
    }

    


}
